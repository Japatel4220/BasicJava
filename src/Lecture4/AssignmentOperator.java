package Lecture4;

public class AssignmentOperator {
    public static void main(String[] args) {
        // assignment

        int a = 5; // is a variable int datatype = assignment operator 5 is value
        a = 10;
        // +=
        a += 10; // a= a+10 (answer  is 20 which means you're adding)
        System.out.println(a);

        // -=
        a-=5;   // a=a-5
        System.out.println(a);

        // *=
        a *= 5;  // a= a*5
        System.out.println(a);

        // /=
        a/=5;  //a = a/5
        System.out.println(a);

        // %=
        a%=7;  // a= a%7
        System.out.println(a);
    }
}
