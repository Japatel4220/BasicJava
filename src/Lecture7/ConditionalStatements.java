package Lecture7;

public class ConditionalStatements {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 30;

        if (a > b && a > c) {
            System.out.println("a is biggest");
        } else {
            System.out.println("b is biggest");
        }
    }
}



