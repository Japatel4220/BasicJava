package Lecture7;

public class DoWhile {
    public static void main(String[] args) {
         //while loop -  run the code again till the condition is true
        //first it checks the condition and than run the statement( entry control loop)
        //  while loop is also known as entry control loop

        // do while loop -- first run the statement and then check the condition( exit control loop)

        int a = 1;
        do {
            System.out.println("Jahanvi");
            a++;
        }while (a<=5);


    }
}
